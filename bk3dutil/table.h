
struct SConv {
    char * englishName;
    unsigned char *jpName;
};
bool convertJtoEn(SConv *table, char *name)
{
    int n = 0;
    int s = 0;
    SConv *candidate = NULL;
    while(table->jpName)
    {
        int i;
        for(i=0; name[i]!=0; i++)
        {
            if(i >= table->jpName[0])
            {
                candidate = table;
                s = i;
            }
            if(table->jpName[i+1] != (unsigned char)name[i])
                break;
        }
        if((i == table->jpName[0])&&(name[i] == '\0')) {
            strcpy(name, table->englishName);
            return true;
        }
        table++;
        n++;
    }
    if(candidate)
    {
        char alternateName[60]; // create a name with the end of non-decoded japanese words (might be numbers)
        sprintf(alternateName,"%s%s",candidate->englishName, name+s);
        assert(strlen(alternateName) <= 20);
        strcpy(name, alternateName);
    }
    return false;
}
